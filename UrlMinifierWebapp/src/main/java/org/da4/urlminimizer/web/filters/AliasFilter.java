package org.da4.urlminimizer.web.filters;

import java.io.IOException;

import javax.servlet.DispatcherType;
import javax.servlet.Filter;
import javax.servlet.FilterChain;
import javax.servlet.FilterConfig;
import javax.servlet.ServletException;
import javax.servlet.ServletRequest;
import javax.servlet.ServletResponse;
import javax.servlet.annotation.WebFilter;
import javax.servlet.http.HttpServletRequest;

/**
 * Servlet Filter implementation class AliasFilter
 * This handles all urls, determines whether they are an alias
 */
@WebFilter(dispatcherTypes = {
				DispatcherType.REQUEST, 
				DispatcherType.FORWARD
		}
					, urlPatterns = { "/*" })
public class AliasFilter implements Filter {

    /**
     * Default constructor. 
     */
    public AliasFilter() {
        // TODO Auto-generated constructor stub
    }

	/**
	 * @see Filter#destroy()
	 */
	public void destroy() {
		// TODO Auto-generated method stub
	}

	/**
	 * @see Filter#doFilter(ServletRequest, ServletResponse, FilterChain)
	 */
	public void doFilter(ServletRequest request, ServletResponse response, FilterChain chain) throws IOException, ServletException {
		String reqUrl = (((HttpServletRequest) request).getServletPath()).substring(1);
		//handle normal urls. . means a file, so don't send to redirector
		if(reqUrl.contains(".")) 
		{
			chain.doFilter(request, response);
			return;
		} else if(reqUrl.contains("restapi"))
		{
			chain.doFilter(request, response);
			return;
		}
		request.setAttribute("URL_ALIAS", reqUrl);
	    request.getRequestDispatcher("/Redirector.do").forward(request, response);
	    
	}

	/**
	 * @see Filter#init(FilterConfig)
	 */
	public void init(FilterConfig fConfig) throws ServletException {
		// TODO Auto-generated method stub
	}

}
